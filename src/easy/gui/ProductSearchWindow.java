    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchtext = txtSearch.getText();

        try {
            ProductDAO productdao = new ProductDAO();
            ArrayList<Product> items = new ArrayList<>();
            if (searchtext != null && searchtext.trim().length() > 0) {
                items = productdao.searchProduct(searchtext);
            } else {
                items = productdao.getAllProduct();
            }
            // Using table Model
            ProductTableModel model = new ProductTableModel(items);
            tblList.setModel(model);

            this.updateLables();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSellActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSellActionPerformed
        try {
            ProductDAO productdao = new ProductDAO();
            int row = tblList.getSelectedRow();

            if (row < 0) {
                JOptionPane.showMessageDialog(null, "Error", "You must select a Product.", JOptionPane.ERROR_MESSAGE);

                return;
            }
            int response = JOptionPane.showConfirmDialog(null, "Sell this products?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (response != JOptionPane.YES_OPTION) {
                return;
            }

            Product tmpProduct = (Product) tblList.getValueAt(row, ProductTableModel.OBJECT_COL);

            try {
                productdao.soldItem(tmpProduct.getId());
                updateLables();
            } catch (SQLException e1) {
                e1.printStackTrace();
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSellActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            ProductDAO productdao = new ProductDAO();
            int row = tblList.getSelectedRow();

            if (row < 0) {
                JOptionPane.showMessageDialog(null, "Error", "You must select a Product.", JOptionPane.ERROR_MESSAGE);

                return;
            }
            int response = JOptionPane.showConfirmDialog(null, "Delete this product?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (response != JOptionPane.YES_OPTION) {
                return;
            }

            Product tmpProduct = (Product) tblList.getValueAt(row, ProductTableModel.OBJECT_COL);

            try {
                productdao.deleteItem(tmpProduct.getId());
                updateLables();
            } catch (SQLException e1) {
                e1.printStackTrace();
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            ProductDAO productdao = new ProductDAO();
            int row = tblList.getSelectedRow();

            if (row < 0) {
                JOptionPane.showMessageDialog(null, "You must select a Product.", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            Product tmpProduct = (Product) tblList.getValueAt(row, ProductTableModel.OBJECT_COL);

            //System.out.println(tmpProduct);
            AddNewProductItem dialog = new AddNewProductItem(productdao, ProductSearchWindow.this, tmpProduct, true);

            dialog.setVisible(true);

            ProductDAO productdao1 = new ProductDAO();
            ArrayList<Product> items = new ArrayList<>();
            if (txtSearch.getText() != null && txtSearch.getText().trim().length() > 0) {
                items = productdao1.searchProduct(txtSearch.getText());
            } else {
                items = productdao1.getAllProduct();
            }
            // Using table Model
            ProductTableModel model = new ProductTableModel(items);
            tblList.setModel(model);

            this.updateLables();

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

