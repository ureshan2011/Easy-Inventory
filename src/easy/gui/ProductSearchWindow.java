    void updateLables() {
        try {
            ProductDAO productdao = new ProductDAO();

            String totalItems = productdao.getTotalItems();
            lbltotItems.setText(totalItems);

            String availableItems = productdao.getAvaiItems();
            lblAvailableItems.setText(availableItems);

            String soldItems = productdao.getSoldItems();
            lblSoldItems.setText(soldItems);

            String totalInvestment = productdao.getTotalInvest();
            lblTotInves.setText(totalInvestment);

            String totalSales = productdao.getTotalSell();
            lblTotSales.setText(totalSales);

            String totalProfits = productdao.getProfit();
            lblTotProfits.setText(totalProfits);

        } catch (Exception e) {
            System.out.println(e);
        }

    }

